class Calculator {

    // add() – adds two integers
    int add(int a, int b) {
        return a + b;
    }

    // multiply() – multiplies two integers
    int multiply(int a, int b) {
        return a * b;
    }

    // square() – returns square of a number
    int square(int a) {
        return a * a;
    }
}
